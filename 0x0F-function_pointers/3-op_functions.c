#include "3-calc.h"
#include <stdlib.h>
/**
 * op_add - Return the sum of a b
 * @a: input num1
 * @b: input num2
 * Return: integer with result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Return the substract of a b
 * @a: input num1
 * @b: input num2
 * Return: integer with result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Return the multiply of a b
 * @a: input num1
 * @b: input num2
 * Return: integer with result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Return the divide of a b
 * @a: input num1
 * @b: input num2
 * Return: integer with result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Return the mod of a b
 * @a: input num1
 * @b: input num2
 * Return: integer with result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
